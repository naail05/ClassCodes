package day02_escapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello everyone Today we are practicing escape sequences");
        System.out.println();
        System.out.println("Hello everyone\nToday we are practicing escape sequences");
        System.out.println();
        System.out.println("\t now practicing the second escape sequence, giving paragraph space");
        System.out.println();
        System.out.println(" \\ for printing \"single back slash\"");
        System.out.println();
        System.out.println(" \" for printing \"double quotes\"");
        /* when we use error print method the printed code will come
        at the end in the console, irrespective of where its written
         */
        /* we used four escape sequences here:
         1. \n for starting a new line
         2. \t for giving paragraph space
         3.\\ for giving or printing backslash
         4.\" for printing double quote
         5. \"\" for printing two double quotes
         */


    }
}
