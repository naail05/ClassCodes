package day02_escapeSequences;

public class EmployeeInfo { // This is class header

    public static void main(String[] args) { /* This is main method, used to make our class or
    java file runnable, here we used multi line comment */


        System.out.println("Name:      Bashir Naail"); //This is print statement
        System.out.println("Gender:    Male");
        System.out.println("Age:       35");
        System.out.println("Job Title: Java SDET");
        System.out.println("Company:   Google");
        System.out.println("Salary:    $135k");
        System.out.println();

        System.out.println("Name:\t    Bashir Naail\nGender:\t    Male  \nAge:\t    35\nJob Title:\t    Java SDET\nCompany:\t    Google\nSalary:    \t  $135k");


    }
}
